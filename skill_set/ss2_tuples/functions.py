def display_tuple(tpl):
    print(tpl)

def unpack_tuple(tpl):
    elem1, elem2, elem3, elem4 = tpl
    print(f"elem1={elem1}, elem2={elem2}, elem3={elem3}, elem4={elem4}")

def display_length(tpl):
    print(len(tpl))

def display_third_element(tpl):
    print(tpl[2])

def display_slice(tpl):
    print(tpl[1:3])
